import test from "node:test";
import assert from "node:assert/strict";
import { detectImageDimensions } from "../src/core/image-dimension-utils";

test("detectImageDimensions reads PNG size from IHDR", () => {
  const data = Uint8Array.from([
    0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a,
    0x00, 0x00, 0x00, 0x0d,
    0x49, 0x48, 0x44, 0x52,
    0x00, 0x00, 0x00, 0x20,
    0x00, 0x00, 0x00, 0x10
  ]);

  assert.deepEqual(detectImageDimensions(data), { width: 32, height: 16 });
});

test("detectImageDimensions reads GIF size from logical screen descriptor", () => {
  const data = Uint8Array.from([
    0x47, 0x49, 0x46, 0x38, 0x39, 0x61,
    0x2a, 0x00,
    0x18, 0x00
  ]);

  assert.deepEqual(detectImageDimensions(data), { width: 42, height: 24 });
});

test("detectImageDimensions reads JPEG size from SOF marker", () => {
  const data = Uint8Array.from([
    0xff, 0xd8,
    0xff, 0xe0, 0x00, 0x10,
    0x4a, 0x46, 0x49, 0x46, 0x00, 0x01, 0x01, 0x01,
    0x00, 0x48, 0x00, 0x48, 0x00, 0x00,
    0xff, 0xc0, 0x00, 0x11,
    0x08,
    0x00, 0x30,
    0x00, 0x40,
    0x03,
    0x01, 0x11, 0x00,
    0x02, 0x11, 0x00,
    0x03, 0x11, 0x00
  ]);

  assert.deepEqual(detectImageDimensions(data), { width: 64, height: 48 });
});

test("detectImageDimensions reads BMP size from BITMAPINFOHEADER", () => {
  const data = Uint8Array.from([
    0x42, 0x4d,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00,
    0x00, 0x00,
    0x36, 0x00, 0x00, 0x00,
    0x28, 0x00, 0x00, 0x00,
    0x80, 0x00, 0x00, 0x00,
    0x20, 0x00, 0x00, 0x00
  ]);

  assert.deepEqual(detectImageDimensions(data), { width: 128, height: 32 });
});

test("detectImageDimensions returns undefined for unsupported data", () => {
  const data = Uint8Array.from([0x00, 0x01, 0x02, 0x03, 0x04]);
  assert.equal(detectImageDimensions(data), undefined);
});
